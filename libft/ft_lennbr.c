#include "libft.h"

int		ft_lennbr(int n)
{
	int i;
	int min;

	i = 1;
	min = n < 0;
	if (min)
	{
		n = n / 10 * (-1);
		if (n > 0)
			i++;
	}
	while (n / 10 > 0)
	{
		n = n / 10;
		i++;
	}
	return (i + min);
}
